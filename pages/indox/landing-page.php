                            <h5>Who can be a Red Cross Member?</h5>
                            <p>Individuals aged 6-65 years old (students, out-of-school youths, employees, utility workers, Red Cross staff, volunteers and trainees, etc.)</p>
                            <h5>How Does One Become a Red Cross Member?</h5>
                            <p>Register at any Red Cross Chapter nationwide and contribute an annual membership fee of P50.00, P120.00, P300.00, P500.00 or P1, 000.00 depending on your choice of membership category.</p>
                            <h5>To become a Red Cross Member, please call 790-2300 local 114 or email membership@redcross.org.ph, or you may visit any Philippine Red Cross Chapter.</h5>
                            <p>1. Classic Membership - this type aims to continue a dynamic generation of young blood in revitalizing the organization in serving the most vulnerable. Open to all students and youth aged 6-25 years old.</p>
                            <p>2. Premier Membership (Bronze, Silver, Gold and Platinum) - this type is to get commitment from individuals who believe in building a proactive membership movement with the Red Cross. This further encourages community members to be socially responsible. Open to ages 6-65 years old.</p>
                            
