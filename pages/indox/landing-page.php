                            <h3>Why Join the Red Cross?</h3>
                            <h5>Self worth</h5>
                            <p>Being a member of the Red Cross gives an individual a sense of worth since he/she becomes a part of the premiere humanitarian organization serves the most vulnerable.<br/></p>
                            <h5>The professional networking and friendship</h5>
                            <p>Red Cross provides forums and activities for professional, business leaders and youth (e.g. fundraising activities and Red Cross Youth Program).</p>
                            <h5>Personal Growth and Development</h5>
                            <p>PRC members may avail of leadership, disaster response, first aid and other youth development training. Red Cross expansive network of chapters also provides extensive opportunities for service and interchange.</p>
                            <h5>Access to Red Cross services</h5>
                            <p>During emergencies, PRC members will have the access to safe blood, safety and other life saving services of the PRC. Other services, on the other hand, require fees to augment direct operational costs.</p>
